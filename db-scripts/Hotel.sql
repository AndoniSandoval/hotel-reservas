CREATE TABLE USUARIOS (
    ID_USUARIO      NUMBER(10) GENERATED BY DEFAULT AS IDENTITY,
    USERNAME        VARCHAR2(20) NOT NULL,
    PASSWORD        VARCHAR2(255) NOT NULL,
    ROL             VARCHAR2(10) NOT NULL,
    ESTADO_REGISTRO VARCHAR2(10) NOT NULL,
    FECHA_CREACION  DATE DEFAULT SYSDATE NOT NULL,
    FECHA_ACTUALIZACION DATE,

    CONSTRAINT USUARIO_PK PRIMARY KEY (ID_USUARIO),

    CONSTRAINT UK_USERNAME UNIQUE (USERNAME),

    CONSTRAINT CHECK_ROL_USUARIO
        CHECK (ROL IN ('ADMIN', 'USER')),

    CONSTRAINT CHECK_ESTADO_USUARIO
        CHECK (ESTADO_REGISTRO IN ('ACTIVO', 'ELIMINADO')),

    CONSTRAINT CHECK_PASSWORD_LENGTH
        CHECK (LENGTH(PASSWORD) >= 8),

    CONSTRAINT CHECK_USERNAME_LENGTH
        CHECK (LENGTH(USERNAME) BETWEEN 5 AND 20)
);

CREATE TABLE HUESPEDES (
    ID_HUESPED      NUMBER(10) GENERATED BY DEFAULT AS IDENTITY,
    NOMBRE          VARCHAR2(50) NOT NULL,
    APELLIDO_PATERNO VARCHAR2(50) NOT NULL,
    APELLIDO_MATERNO VARCHAR2(50),
    EMAIL           VARCHAR2(100) NOT NULL,
    TELEFONO        VARCHAR2(10) NOT NULL,
    DOCUMENTO       VARCHAR2(30) NOT NULL,
    NACIONALIDAD    VARCHAR2(50) NOT NULL,
    ESTADO_REGISTRO VARCHAR2(10) NOT NULL,
    FECHA_CREACION  DATE DEFAULT SYSDATE NOT NULL,
    FECHA_ACTUALIZACION DATE,

    CONSTRAINT HUESPED_PK PRIMARY KEY (ID_HUESPED),

    CONSTRAINT CHECK_ESTADO_HUESPED
        CHECK (ESTADO_REGISTRO IN ('ACTIVO', 'ELIMINADO')),

    CONSTRAINT CHECK_NOMBRE_LENGTH
        CHECK (LENGTH(NOMBRE) BETWEEN 2 AND 50),

    CONSTRAINT CHECK_APELLIDO_PATERNO_LENGTH
        CHECK (LENGTH(APELLIDO_PATERNO) BETWEEN 2 AND 50),

    CONSTRAINT CHECK_APELLIDO_MATERNO_LENGTH
        CHECK (LENGTH(APELLIDO_MATERNO) BETWEEN 2 AND 50),

    CONSTRAINT CHECK_TELEFONO_FORMAT
        CHECK (REGEXP_LIKE(TELEFONO, '^[0-9]{10}$'))
);

CREATE TABLE HABITACIONES (
    ID_HABITACION        NUMBER(10) GENERATED BY DEFAULT AS IDENTITY,
    NUMERO_HABITACION    NUMBER(5) NOT NULL,
    TIPO_HABITACION      VARCHAR2(30) NOT NULL,
    PRECIO_NOCHE         NUMBER(10,2) NOT NULL,
    CAPACIDAD            NUMBER(2) NOT NULL,
    ESTADO_HABITACION    VARCHAR2(15) DEFAULT 'DISPONIBLE' NOT NULL,
    ESTADO_REGISTRO      VARCHAR2(10) NOT NULL,
    FECHA_CREACION       DATE DEFAULT SYSDATE NOT NULL,
    FECHA_ACTUALIZACION  DATE,

    CONSTRAINT HABITACION_PK PRIMARY KEY (ID_HABITACION),

    CONSTRAINT UK_NUMERO_HABITACION UNIQUE (NUMERO_HABITACION),
/* 
    CONSTRAINT CHECK_TIPO_HABITACION
        CHECK (TIPO_HABITACION IN ('SENCILLA', 'DOBLE', 'SUITE')), */

    CONSTRAINT CHECK_PRECIO_NOCHE
        CHECK (PRECIO_NOCHE > 0),

    CONSTRAINT CHECK_CAPACIDAD
        CHECK (CAPACIDAD >= 1),

    CONSTRAINT CHECK_ESTADO_HABITACION
        CHECK (ESTADO_HABITACION IN ('DISPONIBLE','OCUPADA','LIMPIEZA','MANTENIMIENTO')),

    CONSTRAINT CHECK_ESTADO_REGISTRO_HAB
        CHECK (ESTADO_REGISTRO IN ('ACTIVO','ELIMINADO'))
);

CREATE TABLE RESERVAS (
    ID_RESERVA          NUMBER(10) GENERATED BY DEFAULT AS IDENTITY,
    ID_HUESPED          NUMBER(10) NOT NULL,
    ID_HABITACION       NUMBER(10) NOT NULL,
    FECHA_ENTRADA       DATE NOT NULL,
    FECHA_SALIDA        DATE NOT NULL,
    ESTADO_RESERVA      VARCHAR2(15) DEFAULT 'CONFIRMADA' NOT NULL,
    ESTADO_REGISTRO     VARCHAR2(10) NOT NULL,
    FECHA_CREACION      DATE DEFAULT SYSDATE NOT NULL,
    FECHA_ACTUALIZACION DATE,

    CONSTRAINT RESERVA_PK PRIMARY KEY (ID_RESERVA),

    CONSTRAINT CHECK_ESTADO_RESERVA
        CHECK (ESTADO_RESERVA IN ('CONFIRMADA','EN_CURSO','FINALIZADA','CANCELADA')),

    CONSTRAINT CHECK_ESTADO_REGISTRO_RES
        CHECK (ESTADO_REGISTRO IN ('ACTIVO','ELIMINADO')),

    CONSTRAINT CHECK_FECHAS
        CHECK (FECHA_SALIDA > FECHA_ENTRADA)
);